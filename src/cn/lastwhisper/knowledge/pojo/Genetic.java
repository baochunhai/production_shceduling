package cn.lastwhisper.knowledge.pojo;

public class Genetic {
    private String geneNumber;

    private String generation;

    private String gap;

    private String cross;

    private String aberrance;

    public String getGeneNumber() {
        return geneNumber;
    }

    public void setGeneNumber(String geneNumber) {
        this.geneNumber = geneNumber;
    }

    public String getGeneration() {
        return generation;
    }

    public void setGeneration(String generation) {
        this.generation = generation;
    }

    public String getGap() {
        return gap;
    }

    public void setGap(String gap) {
        this.gap = gap;
    }

    public String getCross() {
        return cross;
    }

    public void setCross(String cross) {
        this.cross = cross;
    }

    public String getAberrance() {
        return aberrance;
    }

    public void setAberrance(String aberrance) {
        this.aberrance = aberrance;
    }
}
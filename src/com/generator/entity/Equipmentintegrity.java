package com.generator.entity;

public class Equipmentintegrity {
    private String mpsno;

    private String processno;

    private String fstarttime;

    private String precoverytime;

    private String fendtime;

    private String fdescription;

    private String status;

    public String getMpsno() {
        return mpsno;
    }

    public void setMpsno(String mpsno) {
        this.mpsno = mpsno;
    }

    public String getProcessno() {
        return processno;
    }

    public void setProcessno(String processno) {
        this.processno = processno;
    }

    public String getFstarttime() {
        return fstarttime;
    }

    public void setFstarttime(String fstarttime) {
        this.fstarttime = fstarttime;
    }

    public String getPrecoverytime() {
        return precoverytime;
    }

    public void setPrecoverytime(String precoverytime) {
        this.precoverytime = precoverytime;
    }

    public String getFendtime() {
        return fendtime;
    }

    public void setFendtime(String fendtime) {
        this.fendtime = fendtime;
    }

    public String getFdescription() {
        return fdescription;
    }

    public void setFdescription(String fdescription) {
        this.fdescription = fdescription;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}